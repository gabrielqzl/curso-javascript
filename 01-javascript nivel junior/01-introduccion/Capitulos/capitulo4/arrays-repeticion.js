// Metodos de Arrays
// De repetición
// -filter() - crea un nuevo array con todos los elementos
// forEach() - Ejecuta una función indicada una vez por cada



let numeros = ["abecedario", "manzana", "pedro", "dedo", "bobo"]

let resultado = numeros.filter(numero => numero.length > 5)
numeros.forEach((numero) => document.write(numero+", "))
document.write("<br>"+resultado)
